export function getTrans(lang: string = 'en') {
    // Fallback to 'en' if lang is not hi/mr
    const key = (Object.keys(ASTRO_TRANSLATIONS) as string[]).includes(lang) ? lang : 'en';
    return ASTRO_TRANSLATIONS[key as keyof typeof ASTRO_TRANSLATIONS];
}

export function translatePlanet(name: string, lang: string) {
    const t = getTrans(lang);
    return t.planets[name as keyof typeof t.planets] || name;
}

export function translateSign(name: string, lang: string) {
    const t = getTrans(lang);
    return t.signs[name as keyof typeof t.signs] || name;
}
